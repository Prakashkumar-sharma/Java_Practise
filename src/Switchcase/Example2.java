package Switchcase;

public class Example2 {
    public static void main(String[] args) {
        char c = 'D';
        switch (c){
            case 'A':
                System.out.println("A");
                break;

            case 'B':
                System.out.println("B");
                break;

            case 'C':
                System.out.println("C");
                break;

            case 'D':
                System.out.println("D");
                break;

            case 'E':
                System.out.println("Default");
                break;


        }
    }
}
