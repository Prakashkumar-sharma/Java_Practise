package Ifelse;

public class Variableconditionex2 {
    public static void main(String[] args) {
        int i = 20;
        int j;
        if(i == 20){
            j = 50;
        }
        else if (i == 30) {
            j = 100;
        }
        else{
            j = 150;
        }
        System.out.println(j);
    }
}
